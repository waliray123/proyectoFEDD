    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Ingresar
        ControlCrud controlCrud = new ControlCrud(this.guardadoDatos);
        if (this.tipoAccion.equals("insertar")) {
            //Insertar
            controlCrud.insertarCatedratico(this.jTextField3.getText(), this.jTextField1.getText(), this.jTextField2.getText());
            limpiar();
        } else if (this.tipoAccion.equals("eliminar")) {
            //Eliminar
            controlCrud.eliminarCatedratico(catedraticoUsado);
            limpiar();
        } else if (this.tipoAccion.equals("modificar")) {
            //Modificar
            controlCrud.modificarCatedratico(this.jTextField1.getText(), this.jTextField2.getText(), catedraticoUsado);
            limpiar();
        }
    }//GEN-LAST:event_jButton4ActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.tipoAccion = "eliminar";
        String carnet = JOptionPane.showInputDialog("Ingrese el codigo del catedratico");
        this.catedraticoUsado = this.guardadoDatos.buscarCatedraticoPorCodigo(carnet);
        if (this.catedraticoUsado != null) {
            this.jTextField1.setText(this.catedraticoUsado.getNombre());
            this.jTextField2.setText(this.catedraticoUsado.getDireccion());
            this.jTextField3.setText(this.catedraticoUsado.getIdentificador());    
            this.jPanel1.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "No existe el Catedratico");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        this.tipoAccion = "modificar";
        String carnet = JOptionPane.showInputDialog("Ingrese el codigo del catedratico");
        this.catedraticoUsado = this.guardadoDatos.buscarCatedraticoPorCodigo(carnet);
        if (this.catedraticoUsado != null) {
            this.jTextField1.setText(this.catedraticoUsado.getNombre());
            this.jTextField2.setText(this.catedraticoUsado.getDireccion());
            this.jTextField3.setText(this.catedraticoUsado.getIdentificador());        
            this.jTextField3.setEnabled(false);
            this.jPanel1.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "No existe el Catedratico");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void limpiar(){        
        this.jTextField3.setEnabled(true);     
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");        
        this.catedraticoUsado = null;
        this.tipoAccion = "";
        this.jPanel1.setVisible(false);
    }
    
